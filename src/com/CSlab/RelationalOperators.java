package com.CSlab;

public class RelationalOperators {
    public static void main(String[] args){
        int x = 5,y =8;
        System.out.print( "x==y: ");
        System.out.println(x==y);
        System.out.print( "x>y: ");
        System.out.println(x>y);
        System.out.print( "x<y: ");
        System.out.println(x<y);
        System.out.print( "x>=y: ");
        System.out.println(x>=y);
        System.out.print( "x!=y: ");
        System.out.println(x!=y);
        System.out.print( "x<=y: ");
        System.out.print(x<=y);

    }

}
