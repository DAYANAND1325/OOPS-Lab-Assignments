package com.CSlab;

public class AssignmentOperators {
    public static void main(String[] args){
        int x= 12, y =5;
        System.out.print( "x+=y: ");
        System.out.println(x+=y);
        System.out.print( "x=x+y: ");
        System.out.println(x=x+y);
        System.out.println();
        System.out.print( "x+=y: ");
        System.out.println(x+=y);
        System.out.print( "x-=y: ");
        System.out.println(x-=y);
        System.out.print( "x*=y: ");
        System.out.println( x*=y);
        System.out.print( "x/=y: ");
        System.out.println(x/=y);
        System.out.print( "x%=y: ");
        System.out.print( x%=y);
    }
}
